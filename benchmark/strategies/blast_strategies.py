#!/usr/bin/env python3
"""Five BLAST distribution strategies for AKS benchmarking.

Each strategy generates a set of INI configs and K8s job specs.
"""

import logging
import os
from abc import ABC, abstractmethod
from dataclasses import dataclass, field

log = logging.getLogger(__name__)


@dataclass
class JobSpec:
    """A single BLAST job to execute on AKS."""
    job_id: str
    query_file: str         # Blob URL or local path to query FASTA
    db_path: str            # BLAST DB name (relative to mount)
    db_source: str          # Blob URL for DB download
    blast_program: str
    blast_options: str
    node_selector: str = '' # Target specific node (for shard affinity)
    dbsize: int = 0         # Override DB size for E-value correction


@dataclass
class StrategyPlan:
    """Execution plan generated by a strategy."""
    name: str
    description: str
    jobs: list[JobSpec]
    num_nodes: int
    db_downloads: dict[str, str]  # node_label -> db_source_url
    needs_merge: bool             # Whether results need re-ranking
    merge_type: str = 'concat'    # 'concat' (query split) or 'rerank' (db shard)
    total_db_io_gb: float = 0.0   # Total DB I/O across all nodes


@dataclass
class BlastConfig:
    """BLAST execution configuration."""
    program: str = 'blastn'
    db_url: str = ''          # Blob URL to DB
    db_name: str = ''         # DB name (e.g., core_nt)
    queries_url: str = ''     # Blob URL to query file
    results_url: str = ''     # Blob URL for results
    options: str = '-max_target_seqs 500 -evalue 0.05 -outfmt "6 std"'
    batch_len: int = 100000
    mem_limit: str = '4G'
    machine_type: str = 'Standard_E64s_v3'
    num_nodes: int = 1
    db_size_gb: float = 500.0
    db_total_letters: int = 0
    query_count: int = 10
    query_total_bases: int = 37746


# ─────────────────────────────────────────────
# Strategy 1: Query Split (current ElasticBLAST)
# ─────────────────────────────────────────────

class QuerySplitStrategy:
    """Split queries into batches, each pod searches full DB.

    This is the existing ElasticBLAST approach.
    Best for: Many queries (1000+), DB fits on single node.
    """
    name = 'query_split'

    def plan(self, cfg: BlastConfig) -> StrategyPlan:
        num_batches = max(1, cfg.query_total_bases // cfg.batch_len)

        jobs = []
        for i in range(num_batches):
            jobs.append(JobSpec(
                job_id=f'batch-{i:03d}',
                query_file=f'{cfg.queries_url}/batch_{i:03d}.fa',
                db_path=cfg.db_name,
                db_source=cfg.db_url,
                blast_program=cfg.program,
                blast_options=cfg.options,
            ))

        # All nodes download full DB
        db_downloads = {f'node-{n}': cfg.db_url for n in range(cfg.num_nodes)}

        return StrategyPlan(
            name=self.name,
            description=(
                f'Query Split: {num_batches} batches × full DB ({cfg.db_size_gb:.0f} GB) '
                f'on {cfg.num_nodes} nodes'
            ),
            jobs=jobs,
            num_nodes=cfg.num_nodes,
            db_downloads=db_downloads,
            needs_merge=False,
            merge_type='concat',
            total_db_io_gb=cfg.db_size_gb * cfg.num_nodes,
        )


# ─────────────────────────────────────────────
# Strategy 2: DB Shard + Full Query Broadcast
# ─────────────────────────────────────────────

class DBShardStrategy:
    """Shard DB across nodes, broadcast all queries to every shard.

    Each node holds 1/N of the DB. Every query runs against every shard.
    Results must be merged and re-ranked (E-value adjusted with -dbsize).

    Best for: Few queries, large DB, need fast response.
    """
    name = 'db_shard'

    def __init__(self, num_shards: int = 10):
        self.num_shards = num_shards

    def plan(self, cfg: BlastConfig) -> StrategyPlan:
        shard_size = cfg.db_size_gb / self.num_shards
        num_nodes = self.num_shards

        # Add -dbsize to options for E-value correction
        options = cfg.options
        if '-dbsize' not in options and cfg.db_total_letters > 0:
            options = f'{options} -dbsize {cfg.db_total_letters}'

        jobs = []
        for s in range(self.num_shards):
            shard_name = f'{cfg.db_name}_shard_{s:02d}'
            jobs.append(JobSpec(
                job_id=f'shard-{s:02d}',
                query_file=cfg.queries_url,  # Full query to each shard
                db_path=shard_name,
                db_source=f'{cfg.db_url}_shards/{shard_name}',
                blast_program=cfg.program,
                blast_options=options,
                node_selector=f'shard-{s:02d}',
                dbsize=cfg.db_total_letters,
            ))

        db_downloads = {
            f'node-{s}': f'{cfg.db_url}_shards/{cfg.db_name}_shard_{s:02d}'
            for s in range(self.num_shards)
        }

        return StrategyPlan(
            name=self.name,
            description=(
                f'DB Shard: {self.num_shards} shards × {shard_size:.0f} GB each, '
                f'full query broadcast, requires merge+re-rank'
            ),
            jobs=jobs,
            num_nodes=num_nodes,
            db_downloads=db_downloads,
            needs_merge=True,
            merge_type='rerank',
            total_db_io_gb=cfg.db_size_gb,  # Each node loads only its shard
        )


# ─────────────────────────────────────────────
# Strategy 3: Hybrid (Query Split × DB Shard)
# ─────────────────────────────────────────────

class HybridStrategy:
    """Combine query splitting and DB sharding for maximum parallelism.

    Total jobs = num_query_batches × num_shards.
    Results grouped by query batch, then merged across shards.

    Best for: Many queries + huge DB (NCBI-scale workloads).
    """
    name = 'hybrid'

    def __init__(self, num_shards: int = 10):
        self.num_shards = num_shards

    def plan(self, cfg: BlastConfig) -> StrategyPlan:
        num_batches = max(1, cfg.query_total_bases // cfg.batch_len)
        shard_size = cfg.db_size_gb / self.num_shards
        total_jobs = num_batches * self.num_shards

        options = cfg.options
        if '-dbsize' not in options and cfg.db_total_letters > 0:
            options = f'{options} -dbsize {cfg.db_total_letters}'

        jobs = []
        for b in range(num_batches):
            for s in range(self.num_shards):
                shard_name = f'{cfg.db_name}_shard_{s:02d}'
                jobs.append(JobSpec(
                    job_id=f'batch-{b:03d}-shard-{s:02d}',
                    query_file=f'{cfg.queries_url}/batch_{b:03d}.fa',
                    db_path=shard_name,
                    db_source=f'{cfg.db_url}_shards/{shard_name}',
                    blast_program=cfg.program,
                    blast_options=options,
                    node_selector=f'shard-{s:02d}',
                    dbsize=cfg.db_total_letters,
                ))

        db_downloads = {
            f'node-{s}': f'{cfg.db_url}_shards/{cfg.db_name}_shard_{s:02d}'
            for s in range(self.num_shards)
        }

        return StrategyPlan(
            name=self.name,
            description=(
                f'Hybrid: {num_batches} batches × {self.num_shards} shards '
                f'= {total_jobs} jobs, {shard_size:.0f} GB/shard'
            ),
            jobs=jobs,
            num_nodes=self.num_shards,
            db_downloads=db_downloads,
            needs_merge=True,
            merge_type='rerank',
            total_db_io_gb=cfg.db_size_gb,
        )


# ─────────────────────────────────────────────
# Strategy 4: Taxonomy Subset
# ─────────────────────────────────────────────

class TaxonomyStrategy:
    """Use taxonomy-filtered subset DB to reduce search space.

    Pre-builds a subset DB containing only relevant organisms.
    Uses standard query-split for execution (no merge changes needed).

    Best for: Known target organisms, pathogen detection.
    """
    name = 'taxonomy'

    def __init__(self, taxids: list[int] = None, subset_name: str = 'pathogen'):
        from .db_prep import TAXIDS
        self.taxids = taxids or [TAXIDS['virus'], TAXIDS['plasmodium']]
        self.subset_name = subset_name

    def plan(self, cfg: BlastConfig) -> StrategyPlan:
        # Estimated subset size (virus + plasmodium ≈ 2-6% of core_nt)
        subset_ratio = 0.05  # Conservative estimate
        subset_size = cfg.db_size_gb * subset_ratio
        subset_db = f'{cfg.db_name}_{self.subset_name}'

        num_batches = max(1, cfg.query_total_bases // cfg.batch_len)

        jobs = []
        for i in range(num_batches):
            jobs.append(JobSpec(
                job_id=f'batch-{i:03d}',
                query_file=f'{cfg.queries_url}/batch_{i:03d}.fa',
                db_path=subset_db,
                db_source=f'{cfg.db_url}_{self.subset_name}',
                blast_program=cfg.program,
                blast_options=cfg.options,
                # No -dbsize needed: subset DB has correct statistics
            ))

        db_downloads = {
            f'node-{n}': f'{cfg.db_url}_{self.subset_name}'
            for n in range(cfg.num_nodes)
        }

        return StrategyPlan(
            name=self.name,
            description=(
                f'Taxonomy Subset ({self.subset_name}): ~{subset_size:.0f} GB '
                f'({subset_ratio*100:.0f}% of {cfg.db_size_gb:.0f} GB), '
                f'{num_batches} batches on {cfg.num_nodes} nodes. '
                f'Taxids: {self.taxids}'
            ),
            jobs=jobs,
            num_nodes=cfg.num_nodes,
            db_downloads=db_downloads,
            needs_merge=False,
            merge_type='concat',
            total_db_io_gb=subset_size * cfg.num_nodes,
        )

    def prepare_db(self, db_path: str, output_dir: str) -> str:
        """Create the taxonomy subset DB (run before benchmark)."""
        from .db_prep import create_taxonomy_subset
        return create_taxonomy_subset(
            db_path=db_path,
            taxids=self.taxids,
            output_path=os.path.join(output_dir, f'{os.path.basename(db_path)}_{self.subset_name}'),
        )


# ─────────────────────────────────────────────
# Strategy 5: Pre-loaded Persistent Nodes
# ─────────────────────────────────────────────

class PreloadedStrategy:
    """Pre-load DB on persistent nodes for zero cold-start latency.

    Combines with DB sharding for scale-out. Nodes keep DB loaded
    across multiple requests (reuse=true).

    Best for: Service mode with repeated queries, low-latency requirements.
    """
    name = 'preloaded'

    def __init__(self, num_shards: int = 10, reuse: bool = True):
        self.num_shards = num_shards
        self.reuse = reuse

    def plan(self, cfg: BlastConfig) -> StrategyPlan:
        shard_size = cfg.db_size_gb / self.num_shards

        options = cfg.options
        if '-dbsize' not in options and cfg.db_total_letters > 0:
            options = f'{options} -dbsize {cfg.db_total_letters}'

        jobs = []
        for s in range(self.num_shards):
            shard_name = f'{cfg.db_name}_shard_{s:02d}'
            jobs.append(JobSpec(
                job_id=f'shard-{s:02d}',
                query_file=cfg.queries_url,
                db_path=shard_name,
                db_source=f'{cfg.db_url}_shards/{shard_name}',
                blast_program=cfg.program,
                blast_options=options,
                node_selector=f'shard-{s:02d}',
                dbsize=cfg.db_total_letters,
            ))

        db_downloads = {
            f'node-{s}': f'{cfg.db_url}_shards/{cfg.db_name}_shard_{s:02d}'
            for s in range(self.num_shards)
        }

        return StrategyPlan(
            name=self.name,
            description=(
                f'Pre-loaded: {self.num_shards} shards × {shard_size:.0f} GB, '
                f'DB pre-cached on persistent nodes (reuse={self.reuse}), '
                f'zero cold-start for subsequent requests'
            ),
            jobs=jobs,
            num_nodes=self.num_shards,
            db_downloads=db_downloads,
            needs_merge=True,
            merge_type='rerank',
            total_db_io_gb=0.0 if self.reuse else cfg.db_size_gb,
        )

    def generate_daemonset_yaml(self, cfg: BlastConfig) -> str:
        """Generate DaemonSet YAML for DB pre-loading on nodes."""
        shards_config = []
        for s in range(self.num_shards):
            shard_name = f'{cfg.db_name}_shard_{s:02d}'
            shards_config.append(
                f'        - name: preload-shard-{s:02d}\n'
                f'          image: mcr.microsoft.com/azure-cli:latest\n'
                f'          command: ["bash", "-c"]\n'
                f'          args:\n'
                f'            - |\n'
                f'              if [ ! -f /mnt/ssd/{shard_name}.nsq ]; then\n'
                f'                azcopy login --identity;\n'
                f'                azcopy cp "{cfg.db_url}_shards/{shard_name}*" /mnt/ssd/;\n'
                f'              fi\n'
                f'          volumeMounts:\n'
                f'            - name: ssd\n'
                f'              mountPath: /mnt/ssd\n'
            )

        return (
            f'apiVersion: apps/v1\n'
            f'kind: DaemonSet\n'
            f'metadata:\n'
            f'  name: blast-db-preloader\n'
            f'spec:\n'
            f'  selector:\n'
            f'    matchLabels:\n'
            f'      app: blast-db-preloader\n'
            f'  template:\n'
            f'    metadata:\n'
            f'      labels:\n'
            f'        app: blast-db-preloader\n'
            f'    spec:\n'
            f'      initContainers:\n'
            + ''.join(shards_config) +
            f'      containers:\n'
            f'        - name: pause\n'
            f'          image: registry.k8s.io/pause:3.9\n'
            f'      volumes:\n'
            f'        - name: ssd\n'
            f'          hostPath:\n'
            f'            path: /mnt\n'
            f'            type: DirectoryOrCreate\n'
        )


# ─────────────────────────────────────────────
# Strategy Registry
# ─────────────────────────────────────────────

STRATEGIES = {
    'query_split': QuerySplitStrategy,
    'db_shard': DBShardStrategy,
    'hybrid': HybridStrategy,
    'taxonomy': TaxonomyStrategy,
    'preloaded': PreloadedStrategy,
}


def get_strategy(name: str, **kwargs):
    """Get a strategy by name."""
    if name not in STRATEGIES:
        raise ValueError(f'Unknown strategy: {name}. Available: {list(STRATEGIES.keys())}')
    return STRATEGIES[name](**kwargs)


def compare_strategies(cfg: BlastConfig) -> None:
    """Print comparison of all strategies for given config."""
    print(f'\n{"="*70}')
    print(f' Strategy Comparison for {cfg.db_name} ({cfg.db_size_gb:.0f} GB)')
    print(f' Queries: {cfg.query_count} ({cfg.query_total_bases:,} bases)')
    print(f'{"="*70}\n')

    strategies = [
        QuerySplitStrategy(),
        DBShardStrategy(num_shards=10),
        HybridStrategy(num_shards=10),
        TaxonomyStrategy(),
        PreloadedStrategy(num_shards=10),
    ]

    print(f'{"Strategy":<15} {"Jobs":>5} {"Nodes":>6} {"DB I/O":>10} '
          f'{"Merge":>8} {"Best For"}')
    print(f'{"-"*15} {"-"*5} {"-"*6} {"-"*10} {"-"*8} {"-"*30}')

    best_for = {
        'query_split': 'Many queries (1000+)',
        'db_shard': 'Few queries, fast response',
        'hybrid': 'Many queries + huge DB',
        'taxonomy': 'Known target organisms',
        'preloaded': 'Service mode, low latency',
    }

    for s in strategies:
        plan = s.plan(cfg)
        merge_str = plan.merge_type if plan.needs_merge else 'none'
        print(f'{plan.name:<15} {len(plan.jobs):>5} {plan.num_nodes:>6} '
              f'{plan.total_db_io_gb:>8.0f}GB {merge_str:>8} '
              f'{best_for.get(plan.name, "")}')

    print(f'\n{"="*70}\n')

    # Detailed plans
    for s in strategies:
        plan = s.plan(cfg)
        print(f'--- {plan.name} ---')
        print(f'  {plan.description}')
        print(f'  Jobs: {len(plan.jobs)}')
        for j in plan.jobs[:3]:
            print(f'    {j.job_id}: query={os.path.basename(j.query_file)} '
                  f'db={j.db_path}')
        if len(plan.jobs) > 3:
            print(f'    ... ({len(plan.jobs) - 3} more)')
        print()
