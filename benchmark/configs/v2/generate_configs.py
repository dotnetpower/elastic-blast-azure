#!/usr/bin/env python3
"""Generate all benchmark v2 INI configs for each strategy × query scale.

Creates configs for:
  - 5 strategies × 4 query scales = 20 test scenarios
  - Each with appropriate VM SKU, node count, and BLAST options

Usage:
    python benchmark/configs/v2/generate_configs.py
"""

import os

STORAGE = "stgelb"
BLOB_BASE = f"https://{STORAGE}.blob.core.windows.net"
DB_URL = f"{BLOB_BASE}/blast-db/core_nt/core_nt"
QUERIES_BASE = f"{BLOB_BASE}/queries"
RESULTS_BASE = f"{BLOB_BASE}/results/v2"

COMMON_CP = """[cloud-provider]
azure-region = koreacentral
azure-acr-resource-group = rg-elbacr
azure-acr-name = elbacr
azure-resource-group = rg-elb-koc
azure-storage-account = stgelb
azure-storage-account-container = blast-db
"""

BLAST_OPTIONS = "-max_target_seqs 500 -evalue 0.05 -word_size 28 -dust yes -soft_masking true -outfmt 7"

# Query scales
QUERY_SCALES = {
    "10":   {"file": "pathogen-10.fa",   "batch_len": 100000},
    "100":  {"file": "pathogen-100.fa",  "batch_len": 100000},
    "300":  {"file": "pathogen-300.fa",  "batch_len": 100000},
    "1000": {"file": "pathogen-1000.fa", "batch_len": 100000},
}


def write_ini(filepath: str, content: str):
    os.makedirs(os.path.dirname(filepath), exist_ok=True)
    with open(filepath, 'w') as f:
        f.write(content)
    print(f"  {filepath}")


def gen_query_split(out_dir: str):
    """Strategy 1: Query Split — standard ElasticBLAST."""
    print("\n=== Strategy 1: query_split ===")
    for scale, q in QUERY_SCALES.items():
        for nodes in [1, 3]:
            name = f"s1-qsplit-{scale}q-{nodes}n"
            ini = f"""{COMMON_CP}
[cluster]
name = elb-v2-qsplit
machine-type = Standard_E64s_v3
num-nodes = {nodes}
exp-use-local-ssd = true

[blast]
program = blastn
db = {DB_URL}
queries = {QUERIES_BASE}/{q['file']}
results = {RESULTS_BASE}/query_split/{name}
options = {BLAST_OPTIONS}
batch-len = {q['batch_len']}
mem-limit = 4G
"""
            write_ini(f"{out_dir}/query_split/{name}.ini", ini)


def gen_db_shard(out_dir: str):
    """Strategy 2: DB Shard — shard DB across nodes, broadcast queries."""
    print("\n=== Strategy 2: db_shard ===")
    num_shards = 10
    for scale in ["10", "300"]:
        q = QUERY_SCALES[scale]
        name = f"s2-dbshard-{scale}q-{num_shards}sh"
        # Each shard gets its own job; we generate a master config
        # that describes the overall run. Individual shard jobs are
        # generated by the strategy runner.
        ini = f"""{COMMON_CP}
# DB Shard Strategy: {num_shards} shards, full query broadcast
# Each node holds ~50 GB (core_nt / {num_shards})
# Results require merge + re-rank

[cluster]
name = elb-v2-dbshard
machine-type = Standard_E64s_v3
num-nodes = {num_shards}
exp-use-local-ssd = true

[blast]
program = blastn
db = {BLOB_BASE}/blast-db/core_nt_shards/core_nt_shard_00
queries = {QUERIES_BASE}/{q['file']}
results = {RESULTS_BASE}/db_shard/{name}
options = {BLAST_OPTIONS}
batch-len = {q['batch_len']}
mem-limit = 4G

[strategy]
type = db_shard
num-shards = {num_shards}
db-base = {BLOB_BASE}/blast-db/core_nt_shards/core_nt_shard
merge = rerank
"""
        write_ini(f"{out_dir}/db_shard/{name}.ini", ini)


def gen_hybrid(out_dir: str):
    """Strategy 3: Hybrid — query split × DB shard."""
    print("\n=== Strategy 3: hybrid ===")
    num_shards = 10
    for scale in ["300", "1000"]:
        q = QUERY_SCALES[scale]
        name = f"s3-hybrid-{scale}q-{num_shards}sh"
        ini = f"""{COMMON_CP}
# Hybrid Strategy: query split × {num_shards} DB shards
# Maximum parallelism for large workloads

[cluster]
name = elb-v2-hybrid
machine-type = Standard_E64s_v3
num-nodes = {num_shards}
exp-use-local-ssd = true

[blast]
program = blastn
db = {BLOB_BASE}/blast-db/core_nt_shards/core_nt_shard_00
queries = {QUERIES_BASE}/{q['file']}
results = {RESULTS_BASE}/hybrid/{name}
options = {BLAST_OPTIONS}
batch-len = {q['batch_len']}
mem-limit = 4G

[strategy]
type = hybrid
num-shards = {num_shards}
merge = rerank
"""
        write_ini(f"{out_dir}/hybrid/{name}.ini", ini)


def gen_taxonomy(out_dir: str):
    """Strategy 4: Taxonomy — pathogen subset DB."""
    print("\n=== Strategy 4: taxonomy ===")
    subset_db = f"{BLOB_BASE}/blast-db/core_nt_pathogen/core_nt_pathogen"
    for scale, q in QUERY_SCALES.items():
        name = f"s4-taxonomy-{scale}q"
        ini = f"""{COMMON_CP}
# Taxonomy Strategy: pathogen subset DB (~25 GB)
# Virus (10239) + Plasmodium (5820) only
# No merge needed — standard query-split

[cluster]
name = elb-v2-taxonomy
machine-type = Standard_E32s_v3
num-nodes = 1
exp-use-local-ssd = true

[blast]
program = blastn
db = {subset_db}
queries = {QUERIES_BASE}/{q['file']}
results = {RESULTS_BASE}/taxonomy/{name}
options = {BLAST_OPTIONS}
batch-len = {q['batch_len']}
mem-limit = 4G
"""
        write_ini(f"{out_dir}/taxonomy/{name}.ini", ini)


def gen_preloaded(out_dir: str):
    """Strategy 5: Pre-loaded — persistent nodes with DB cached."""
    print("\n=== Strategy 5: preloaded ===")
    num_shards = 10
    for scale in ["10", "300"]:
        q = QUERY_SCALES[scale]
        name = f"s5-preloaded-{scale}q-{num_shards}sh"
        ini = f"""{COMMON_CP}
# Pre-loaded Strategy: {num_shards} shards on persistent nodes
# DB pre-cached, zero cold-start for subsequent requests
# reuse = true keeps cluster alive between runs

[cluster]
name = elb-v2-preloaded
machine-type = Standard_E64s_v3
num-nodes = {num_shards}
exp-use-local-ssd = true
reuse = true

[blast]
program = blastn
db = {BLOB_BASE}/blast-db/core_nt_shards/core_nt_shard_00
queries = {QUERIES_BASE}/{q['file']}
results = {RESULTS_BASE}/preloaded/{name}
options = {BLAST_OPTIONS}
batch-len = {q['batch_len']}
mem-limit = 4G

[strategy]
type = preloaded
num-shards = {num_shards}
merge = rerank
reuse = true
"""
        write_ini(f"{out_dir}/preloaded/{name}.ini", ini)


def main():
    out_dir = os.path.join(os.path.dirname(__file__), "strategies")
    print(f"Generating configs in {out_dir}/")

    gen_query_split(out_dir)
    gen_db_shard(out_dir)
    gen_hybrid(out_dir)
    gen_taxonomy(out_dir)
    gen_preloaded(out_dir)

    # Count total
    total = sum(
        len(files)
        for _, _, files in os.walk(out_dir)
        if files
    )
    print(f"\nTotal: {total} configs generated")


if __name__ == "__main__":
    main()
